package main

import (
	"strings"
)

func generateComponent(fileName string, funcName string, svg []byte) string {
	fn := "// " + funcName + " is the icon from " + fileName + " v" + version + " file\n"
	fn += "func " + funcName + "(children ...gomponents.Node) gomponents.Node {\n"
	fn += "	return gomponents.Raw(`" + string(svg) + "`)\n"
	fn += "}"

	return fn
}

func generatePackage(components []string) string {
	pkg := "// Code generated by lucide-icons build task. DO NOT EDIT.\n"
	pkg += "// v" + version + "\n\n"
	pkg += "package lucide\n\n"
	pkg += "import \"github.com/maragudk/gomponents\"\n\n"
	pkg += strings.Join(components, "\n\n")
	pkg += "\n"

	return pkg
}
